public class Atelier {


    private String outil;
    private String arme;

    public Atelier(String outil, String arme) {
        this.outil = outil;
        this.arme = arme;
    }

    public String getOutil() {
        return outil;
    }

    public void setOutil(String outil) {
        this.outil = outil;
    }

    public String getArme() {
        return arme;
    }

    public void setArme(String arme) {
        this.arme = arme;
    }
}
